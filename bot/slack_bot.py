@app.command("/dev-report")
def handle_dev_report(ack, say, command):
    ack()
    
    try:
        user_text = command.get('text', '').strip()
        repo = user_text if user_text else "microsoft/vscode"
        
        print(f"ğŸ“Š Report requested by: {command['user_name']} for repo: {repo}")
        
        # Send loading message
        say("ğŸ”„ Running LangGraph workflow to generate your engineering report... Please wait!")
        
        # Run LangGraph workflow
        workflow_result = run_workflow(repo)
        
        # Extract results
        insights = workflow_result.get('insights', {})
        narrative = insights.get('narrative', 'No narrative generated')
        recommendations = insights.get('recommendations', [])
        dora_metrics = insights.get('dora_metrics', {})
        
        # Create comprehensive report
        full_report = f"""{narrative}

## Recommendations
"""
        for i, rec in enumerate(recommendations, 1):
            full_report += f"{i}. {rec}\n"
        
        full_report += f"""
## Workflow Summary
âœ… Data Harvester: Collected {len(workflow_result.get('github_data', {}).get('commits', []))} commits
âœ… Diff Analyst: Analyzed code patterns and risks  
âœ… Insight Narrator: Generated DORA metrics and insights

_Generated by FIKA AI LangGraph Workflow_ ğŸ¤–
"""
        
        # Send main report
        say(full_report)
        
        # Create and send charts
        chart_paths = create_comprehensive_charts(workflow_result)
        
        for chart_path in chart_paths:
            if chart_path and os.path.exists(chart_path):
                try:
                    app.client.files_upload_v2(
                        channel=command['channel_id'],
                        file=chart_path,
                        title=f"ğŸ“Š {chart_path.replace('.png', '').replace('_', ' ').title()}",
                        initial_comment=f"Visual analysis for {repo} ğŸ“ˆ"
                    )
                except Exception as e:
                    print(f"Chart upload failed: {e}")
        
        # Log workflow execution
        save_workflow_log("complete_workflow", {"repo": repo}, workflow_result)
        
        print("âœ… LangGraph workflow completed successfully")
                
    except Exception as e:
        print(f"âŒ Error in workflow: {e}")
        say(f"âŒ Sorry, workflow failed: {str(e)}")

